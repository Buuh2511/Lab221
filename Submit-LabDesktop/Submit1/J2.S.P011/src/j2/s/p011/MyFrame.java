    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        // TODO add your handling code here:
        String finalresult;
        if (txtText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty number");
            return;
        }
        num2 = Double.parseDouble(txtText.getText());
        if (caculator == "+") {
            result = num1 + num2;
            finalresult = String.format("%.1f", result);
            txtText.setText(finalresult);
        }
        if (caculator == "-") {
            result = num1 - num2;
            finalresult = String.format("%.1f", result);
            txtText.setText(finalresult);
        }
        if (caculator == "*") {
            result = num1 * num2;
            finalresult = String.format("%.1f", result);
            txtText.setText(finalresult);
        }
        if (caculator == "/") {
            if (num2 == 0) {
                JOptionPane.showMessageDialog(null, "Can't caculate");
                return;
            }
            result = num1 / num2;
            finalresult = String.format("%.1f", result);
            txtText.setText(finalresult);
        }
    }//GEN-LAST:event_jButton15ActionPerformed

