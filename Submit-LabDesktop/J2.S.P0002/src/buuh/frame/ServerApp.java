    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(!checkValid()){
            return;
        }
        File f = new File(txtFilePath.getText());
        try {
            BufferedOutputStream buff = new BufferedOutputStream(new FileOutputStream(f));
            buff.write(fi.getDataByte());
            buff.flush();
            buff.close();
            JOptionPane.showMessageDialog(this, "File has been saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "File has been save");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnChoosePathActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChoosePathActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        try {
            fc.setSelectedFile(new File(txtFilePath.getText()));
            fc.setCurrentDirectory(new File("."));
            fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            fc.showSaveDialog(this);
            if(fc.getSelectedFile() != null){
                txtFilePath.setText(fc.getSelectedFile().getAbsolutePath() + "\\" + fi.getFileName());
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnChoosePathActionPerformed

