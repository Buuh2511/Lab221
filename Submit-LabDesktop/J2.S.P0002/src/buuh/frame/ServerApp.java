    private void startServer(){
        try {
            svSocket = new ServerSocket(PORT);
            socket = svSocket.accept();
            btnStart.setText("is starting");
        } catch (Exception e) {
            e.printStackTrace();
        }
        if(socket != null){
            try {
                br = new BufferedReader(new InputStreamReader(socket.getInputStream()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "network Error");
                System.exit(0);
            }
            t = new Thread((Runnable) this);
            t.start();
        }
    }
    
    private boolean checkValid(){
        File f = new File(txtFilePath.getText().trim());
        if(f.isAbsolute()){
            return true;
        }
        JOptionPane.showMessageDialog(this, "part not valid");
        return false;
    }

