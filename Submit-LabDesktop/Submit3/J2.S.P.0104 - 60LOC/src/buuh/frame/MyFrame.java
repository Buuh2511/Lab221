    private void btnFirstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFirstActionPerformed
        // TODO add your handling code here:
        if(tblStock.getSelectedRow() == 0){
            JOptionPane.showMessageDialog(null, "This is the first row");
            btnFirst.setEnabled(false);
            btnPrevious.setEnabled(false);
        }else{
            tblStock.setRowSelectionInterval(0, 0);
            btnNext.setEnabled(true);
            btnLast.setEnabled(true);
        }
    }//GEN-LAST:event_btnFirstActionPerformed

    private void btnPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviousActionPerformed
        // TODO add your handling code here:
        if(tblStock.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(null, "Choose row!");
        }else if(tblStock.getSelectedRow() == 0){
            tblStock.setRowSelectionInterval(0, 0);
            JOptionPane.showMessageDialog(null, "Can't previous");
            btnPrevious.setEnabled(false);
            btnFirst.setEnabled(false);
        }else{
            tblStock.setRowSelectionInterval((tblStock.getSelectedRow() - 1), (tblStock.getSelectedRow() - 1));
            btnNext.setEnabled(true);
            btnLast.setEnabled(true);
            btnPrevious.setEnabled(true);
            btnFirst.setEnabled(true);
        }
    }//GEN-LAST:event_btnPreviousActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        if(tblStock.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(null, "Choose row!");
        }else if(tblStock.getSelectedRow() == (tblStock.getRowCount() - 1)){
            tblStock.setRowSelectionInterval((tblStock.getRowCount() - 1), (tblStock.getRowCount() - 1));
            JOptionPane.showMessageDialog(null, "Can't next anymore");
            btnLast.setEnabled(false);
            btnNext.setEnabled(false);
        }else{
            tblStock.setRowSelectionInterval((tblStock.getSelectedRow() + 1), (tblStock.getSelectedRow() + 1));
            btnNext.setEnabled(true);
            btnFirst.setEnabled(true);
            btnPrevious.setEnabled(true);
            btnLast.setEnabled(true);
        }
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnLastActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLastActionPerformed
        // TODO add your handling code here:
        if(tblStock.getSelectedRow() == (tblStock.getRowCount() - 1)){
            JOptionPane.showMessageDialog(null, "You are in last row");
            btnLast.setEnabled(false);
            btnNext.setEnabled(false);
        }else{
            tblStock.setRowSelectionInterval((tblStock.getRowCount() - 1), (tblStock.getRowCount() - 1));
            btnFirst.setEnabled(true);
            btnPrevious.setEnabled(true);
        }
    }//GEN-LAST:event_btnLastActionPerformed

