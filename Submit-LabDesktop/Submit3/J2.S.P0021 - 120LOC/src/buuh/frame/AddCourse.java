    boolean isValidNumber(String str){
        int num;
        try {
            num = Integer.parseInt(str);
            if(num < 0 || num > 33){
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Input number");
            return false;
        }
        return true;
    }

