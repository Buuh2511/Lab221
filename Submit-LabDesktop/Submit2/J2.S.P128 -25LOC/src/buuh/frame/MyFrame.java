    private void setImagesforLable(JLabel jl, ImageIcon ic){
        Image im = ic.getImage().getScaledInstance(jl.getHeight(), jl.getWidth(), Image.SCALE_SMOOTH);
        ic = new ImageIcon(im);
        jl.setIcon(ic);
    }
    
    

