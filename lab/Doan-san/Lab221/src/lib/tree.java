package lib;

import javax.swing.JTree;

/**
 *
 * @author DoanNC
 */
public class tree {

    //Expand a tree. Starting with zero for root
    static public void expandAllNodes(JTree tree, int startingIndex, int rowCount) {
        for (int i = startingIndex; i < rowCount; ++i) {
            tree.expandRow(i);
        }

        if (tree.getRowCount() != rowCount) {
            expandAllNodes(tree, rowCount, tree.getRowCount());
        }
    }
}
