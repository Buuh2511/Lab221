public class Calculus {

    int divide(int divider, int num) {
        return divider / num;
    }

    int multiply(int multiplier, int num) {
        return multiplier * num;
    }

    int subtract(int num, int sub) {
        return num - sub;
    }

    int add(int num1, int num2) {
        return num1 + num2;
    }

    int opposite(int num) {
        return num * -1;
    }
}
