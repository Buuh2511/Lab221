    private void loadData() {
        txtAllCourses.setText("");
        for (Course course : list) {
            txtAllCourses.append("\n" + course.toString());
        }
    }
    
    private int checkCredit(String rawCredit) {
        int credit;
        while(true) {
            try {
                credit = Integer.parseInt(rawCredit);
                if (credit < 0 || credit > 33)
                    throw new Exception();
                return credit;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "INVALID CREDIT (0-33)");
                txtCredit.requestFocus();
                return -1;
            }
                
        }
    }
    
