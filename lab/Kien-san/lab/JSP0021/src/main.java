    private void btnSearchCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCourseActionPerformed
        try {
            String searchCode = txtCodeSearch.getText().trim();
            Course searchCourse = DAO.getCourse(searchCode);       
            txtSearchName.setText(searchCourse.getName());
            txtSearchCredit.setText(Integer.toString(searchCourse.getCredit()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "NO COURSE FOUND");
        }
    }//GEN-LAST:event_btnSearchCourseActionPerformed

