    private void bookNameListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_bookNameListValueChanged
        String tmpName = bookNameList.getSelectedValue();
        for (Book book : list) {
            if(book.getBname().equalsIgnoreCase(tmpName)) {
                txtCode.setText(book.getBcode());
                txtName.setText(book.getBname());
                txtAuthor.setText(book.getAuthor());
                txtPublisher.setText(book.getPublisher());
                cbxYear.setSelectedItem(Integer.toString(book.getYear()));
                checkRent.setSelected(book.isRent());
            }
        }
    }//GEN-LAST:event_bookNameListValueChanged

