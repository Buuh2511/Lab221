    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String code = txtCode.getText();
        String name = txtName.getText();
        String author = txtAuthor.getText();
        String publisher = txtPublisher.getText();
        String rawYear = (String)cbxYear.getSelectedItem();
        int year = Integer.parseInt(rawYear);
        boolean isRent = checkRent.isSelected();
        if(addNew) {             
            for (Book book : list) {
                if(book.getBcode().equalsIgnoreCase(code)) {
                    JOptionPane.showMessageDialog(null, "DUPLICATED CODE");
                    txtCode.requestFocus();
                    return;
                }
            }            
            System.out.println(code);
            list.add(new Book(code, name, author, publisher, year, isRent));
            txtCode.setEditable(false);
            JOptionPane.showMessageDialog(null, "Add successfully");
            addNew = false;
                         
        } else { //UPDATE
            //String nameFromList = bookNameList.getSelectedValue();  
            int idx = bookNameList.getSelectedIndex();
            Book tmpBook = list.get(idx);
            for (Book book : list) {
                if(tmpBook.getBcode().trim().equals(book.getBcode())) {                                       
                    book.setBname(name);
                    book.setAuthor(author);
                    book.setPublisher(publisher);
                    book.setYear(year);
                    book.setRent(isRent);
                   JOptionPane.showMessageDialog(null, "UPDATE successfully");
                } else {
                    
                }
            }            
        }       
        model.removeAllElements();
        for (Book book : list) {
            model.addElement(book.getBname());
        }
        bookNameList.setModel(model);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int idx = bookNameList.getSelectedIndex();
        if(idx < 0) {
            JOptionPane.showMessageDialog(null, "Choose a book first before remove it");
            return;
        }
        model.remove(idx);
        list.remove(idx);       
        txtCode.setText("");
        txtName.setText("");
        txtAuthor.setText("");
        txtPublisher.setText("");
        checkRent.setSelected(false);
        JOptionPane.showMessageDialog(null, "Remove Successfully");
    }//GEN-LAST:event_btnRemoveActionPerformed

