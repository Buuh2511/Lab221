    private void resizeImageIcon(ImageIcon icon, JButton btn) {
        Image tmp = icon.getImage().getScaledInstance(btn.getWidth() - 5, btn.getHeight() - 5, java.awt.Image.SCALE_SMOOTH);
        btn.setIcon(new ImageIcon(tmp));
    }
    
    private void resizeImageForLbl(ImageIcon icon, JLabel lbl ) {
        Image tmp = icon.getImage().getScaledInstance(lbl.getWidth(), lbl.getHeight(), java.awt.Image.SCALE_SMOOTH);
        lbl.setIcon(new ImageIcon(tmp));
    }
    
