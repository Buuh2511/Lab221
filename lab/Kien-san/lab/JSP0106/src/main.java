    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        int idx = tblStock.getSelectedRow();
        if(idx < 0) {
            JOptionPane.showMessageDialog(null, "Choose Stock before open it");
            return;
        }
        ChildDialog c = new ChildDialog(this, false);
        c.txtID.setEditable(false);
        c.txtName.setEditable(false);
        c.txtAddress.setEditable(false);
        c.txtDate.setEditable(false);
        c.txtNote.setEditable(false);
        
        c.setVisible(true);
        c.txtID.setText((String) tblStock.getValueAt(idx, 0));
        c.txtName.setText((String)tblStock.getValueAt(idx, 1));
        c.txtAddress.setText((String)tblStock.getValueAt(idx, 2));
        c.txtDate.setText((String)tblStock.getValueAt(idx, 3));
        c.txtNote.setText((String)tblStock.getValueAt(idx, 4));
    }//GEN-LAST:event_btnOpenActionPerformed
    

