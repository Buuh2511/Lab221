    public String formatName(String name) {
        name = name.trim();
        name = name.replaceAll("\\s+"," ");
        name = name.toLowerCase();

        String temp[] = name.split(" ");
        String newName = "";
        
        for (int i = 0; i < temp.length; i++) {
           if (temp[i].charAt(0) < 97 || temp[i].charAt(0) > 122){               
               String str = "";
               str += String.valueOf(temp[i].charAt(0)) + String.valueOf(temp[i].charAt(1)).toUpperCase() +  temp[i].substring(2);
               temp[i] = str;
           }
        }

        for (int i = 0; i < temp.length; i++) {
            newName += String.valueOf(temp[i].charAt(0)).toUpperCase() + temp[i].substring(1);
            if (i < temp.length - 1) {
                newName += " ";
            }
        }
        
        newName = newName.replace("And", "and");
        
        return newName;
    }

