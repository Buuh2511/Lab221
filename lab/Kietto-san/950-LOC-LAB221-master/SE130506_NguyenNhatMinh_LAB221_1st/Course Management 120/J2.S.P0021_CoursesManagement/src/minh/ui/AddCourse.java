    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
        // TODO add your handling code here:
        String code = txtCode.getText();
        String name = txtName.getText();
        String creditStr = txtCredit.getText();
        int credit = Integer.parseInt(creditStr);

        String valid = "";

        if (code.equals("")) {
            valid += "Code cannot be blank! \n";
        }

        if (name.equals("")) {
            valid += "Name cannot be blank! \n";
        }

        if ((creditStr).equals("")) {
            valid += "Credit cannot be blank! \n";
        }

        if (credit < 1 || credit > 33) {
            valid += "Credit must be a positive number and less than or equals to 33!";
        }

        if (valid.equals("")) {
            String newName = formatName(name);  
            CourseDTO dto = new CourseDTO(code, newName, credit);
            CourseDAO dao = new CourseDAO();
            try {
                boolean check = dao.insert(dto);
                if (check){
                    JOptionPane.showMessageDialog(this, "Add successfully!");
                } else {
                    JOptionPane.showMessageDialog(this, "Add failed!");
                }
            } catch (Exception ex) {
                if (ex.getMessage().contains("duplicate")) {
                    JOptionPane.showMessageDialog(this, code + " already exists.");
                } else {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, valid);
        }


    }//GEN-LAST:event_btnAddCourseActionPerformed

