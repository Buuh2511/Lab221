    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        for (int i = 0; i < tblStock.getRowCount(); i++) {
            String strStockID = (String) tblStock.getValueAt(i, 0);
            String strStockName = (String) tblStock.getValueAt(i, 1);
            String strAddress = (String) tblStock.getValueAt(i, 2);
            String strDateAvailable = (String) tblStock.getValueAt(i, 3);
            String strNote = (String) tblStock.getValueAt(i, 4);

            //check ID
            if (strStockID.isEmpty()) {
                JOptionPane.showMessageDialog(this, "StockID is required, insert row" + (i + 1) + " not sucessfully!");
                continue;
            }
            int stockID;
            try {
                stockID = Integer.parseInt(strStockID);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Stock ID: " + strStockID + " is valid, please fill valid StockID");
                continue;
            }
            if (stockID <= 0) {
                JOptionPane.showMessageDialog(this, "StockID: " + strStockID + " is invalid! Please fill stockID greater than 0");
                continue;
            }
          

            //check date
            Date date1 = null;
            try {
                date1 = new SimpleDateFormat("dd-MM-yyyy").parse(strDateAvailable);
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(this, "Date incorrect!");
            }

            java.sql.Date date = new java.sql.Date(date1.getTime());
            StockDTO dto = new StockDTO(stockID, strStockName, strAddress, date, strNote);
  
  //System.out.println(dto);
            StockDAO dao = new StockDAO();
            try {
                dao.Insert(dto);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Cannot insert StockID: " + dto.getStockID());
            }
        }
        JOptionPane.showMessageDialog(this, "Insert successfully!");
    }//GEN-LAST:event_btnInsertActionPerformed

