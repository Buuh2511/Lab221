    public void loadData(){
        try {
            DBDAO dao = new DBDAO();
            DBDTO dto = dao.getInfo();
            txtDtbProductName.setText(dto.getDbProductName());
            txtDtbDriverVersion.setText(dto.getDriverVerson());
            txtDtbProductVersion.setText(dto.getDbProductVerson());
            
            cbListTable.removeAllItems();
            for (int i = 0; i < dto.getListTableinDB().size(); i++) {
                cbListTable.addItem(dto.getListTableinDB().get(i));
            }//add  list dto in to comboBox
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
