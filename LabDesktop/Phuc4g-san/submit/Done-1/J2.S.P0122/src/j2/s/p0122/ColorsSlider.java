    private void jSRedStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSRedStateChanged
        int valueR = Math.round(jSRed.getValue() * 255 / 100);
        int valueG = Math.round(jSGreen.getValue() * 255 / 100);
        int valueB = Math.round(jSBlue.getValue() * 255 / 100);
       try{
            Color color = new Color(valueR, valueG, valueB);
            jLRed.setText("R = " + valueR);
             jLGreen.setText("G = " + valueG);
            jLBlue.setText("B = " + valueB);
            pPaint.setBackground(color);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jSRedStateChanged

