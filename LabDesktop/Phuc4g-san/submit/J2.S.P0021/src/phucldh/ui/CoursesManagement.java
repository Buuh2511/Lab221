    private void btnSearchCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCourseActionPerformed
        // TODO add your handling code here:
        String code = txtSearchCode.getText().trim();
        if (code.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill code!");
            return;
        }

        int pos = findByCode(code);
        if (pos > -1) {
            txtSearchCourseName.setText(list.get(pos).getName());
            txtSearchPrice.setText(Integer.toString(list.get(pos).getPrice()));
            txtSearchCourseName.enable(false);
            txtSearchPrice.enable(false);
        } else {
            JOptionPane.showMessageDialog(this, "Course does not exist!");
        }

    }//GEN-LAST:event_btnSearchCourseActionPerformed

