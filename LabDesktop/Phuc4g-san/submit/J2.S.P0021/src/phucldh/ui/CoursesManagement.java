    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String strCode = txtNewCode.getText().trim();
        String strName = txtNewName.getText().trim();
        String strPrice = txtNewPrice.getText().trim();
        int price = 0;
        // check code
        if (strCode.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Code is not empty!");
            txtNewCode.requestFocus();
            return;
        }
        try {
            if (strCode.matches("e\\d{2}") == false) {
                JOptionPane.showMessageDialog(this, "Code is matches:  'e**'");
                txtNewCode.requestFocus();
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (list != null) {
            for (int i = 0; i < list.size(); i++) {
                if (strCode.equals(list.get(i).getCode().trim())) {
                    JOptionPane.showMessageDialog(this, "Duplicate code!");
                    return;
                }
            }
        }
        

        // check name 
        if (strName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name is not empty!");
            return;
        }

        // check price 
        if (strPrice.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Price is not empty!");
            return;
        }
        try {
            price = Integer.parseInt(strPrice);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price is a number!");
            return;
        }
        if (price <= 0) {
            JOptionPane.showMessageDialog(this, "Price is positive number!");
            return;
        }
        boolean result = false;
        CourseDAO courseDAO = new CourseDAO();
        CourseDTO courseDTO = new CourseDTO(strCode, strName, price);

        try {
            courseDAO.insertCourse(courseDTO);
            result = true;
        } catch (Exception e) {
            e.printStackTrace();
        }

        if (result = true) {
            loadData();
            JOptionPane.showMessageDialog(this, "Add Course successfully!");
            clearText();

        }

    }//GEN-LAST:event_btnAddActionPerformed

