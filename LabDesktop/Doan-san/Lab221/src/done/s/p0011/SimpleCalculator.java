    private void setNum(int num) {
        if (isNewNum) {
            txtResult.setText("0");
            isNewNum = false;
        }
        var tmp = txtResult.getText();
        if (tmp.equals("0")) {
            if (num != 0) {
                txtResult.setText(num + "");
            } else {
                txtResult.setText("0");
            }
        } else {
            if (tmp.length() < maxLength) {
                txtResult.setText(tmp + num);
            }
        }
    }
