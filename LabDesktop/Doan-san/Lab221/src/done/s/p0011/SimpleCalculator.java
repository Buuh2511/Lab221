    private void btnResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultActionPerformed
        // TODO add your handling code here:
        System.out.println(buffer + "");
        if (wasClicked) {
            num = Integer.parseInt(txtResult.getText());
        }
        System.out.println(num + "");
        switch (operator) {
            case "add":
                result = cal.add(buffer, num);
                if (((BigDecimal.valueOf(result)).toString().replace("-", "").length() < 10)) {
                    txtResult.setText((int) result + "");
                } else {
                    txtResult.setText("0");
                    JOptionPane.showMessageDialog(this, "BigDecimal");
                }
                break;
            case "sub":
                if (Integer.parseInt(txtResult.getText()) != 0) {
                    result = cal.subtract(buffer, num);
                    if (((BigDecimal.valueOf(result)).toString().replace("-", "").length() < 10)) {
                        txtResult.setText((int) result + "");
                    } else {
                        txtResult.setText("0");
                        JOptionPane.showMessageDialog(this, "BigDecimal");
                    }
                    break;
                } else {
                    txtResult.setText("0");
                    isNewNum = true;
                }
                break;
            case "mul":
                result = cal.multiply(buffer, num);
                if (((BigDecimal.valueOf(result)).toString().replace("-", "").length() < 10)) {
                    txtResult.setText((int) result + "");
                } else {
                    txtResult.setText("0");
                    JOptionPane.showMessageDialog(this, "BigDecimal");

                }
                break;
            case "div":
                result = cal.divide(buffer, num);
                if (((BigDecimal.valueOf(result)).toString().replace("-", "").length() < 10)) {
                    txtResult.setText((int) result + "");
                } else {
                    txtResult.setText("0");
                    JOptionPane.showMessageDialog(this, "BigDecimal");
                }
                break;
            default:
        }
//        operator = "none";
        buffer = (int) result;
        isNewNum = true;
        wasClicked = false;
    }//GEN-LAST:event_btnResultActionPerformed

    private void btnDivActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDivActionPerformed
        // TODO add your handling code here:
        if (!wasClicked) {
            operator = "none";
        }
        btnResultActionPerformed(null);
        operator = "div";
        buffer = Integer.parseInt(txtResult.getText());
        isNewNum = true;
        wasClicked = true;
    }//GEN-LAST:event_btnDivActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        // TODO add your handling code here:
        if (!txtResult.getText().equalsIgnoreCase("0")) {
            txtResult.setText("0");
        } else {
            if (buffer != 0) {
                buffer = 0;
                operator = "none";
            }
        }
        isNewNum = true;
    }//GEN-LAST:event_btnClearActionPerformed

