    void loadSettings() {
        lblRed.setForeground(Color.RED);
        lblGreen.setForeground(new Color(0, 153, 0));
        lblBlue.setForeground(Color.BLUE);
        sldRed.setMaximum(255);
        sldGreen.setMaximum(255);
        sldBlue.setMaximum(255);
        sldRed.setMinimum(0);
        sldGreen.setMinimum(0);
        sldBlue.setMinimum(0);
        sldRed.setValue(255 / 2);
        sldBlue.setValue(255 / 2);
        sldGreen.setValue(255 / 2);
        changeColor(255 / 2, 255 / 2, 255 / 2);
    }

    void changeColor(int R, int G, int B) {
        panelColor.setBackground(new Color(R, G, B));
        lblRed.setText("Red = " + R);
        lblGreen.setText("Green = " + G);
        lblBlue.setText("Blue = " + B);
    }

