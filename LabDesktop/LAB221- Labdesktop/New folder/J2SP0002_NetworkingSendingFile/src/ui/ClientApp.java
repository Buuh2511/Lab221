    public boolean valid() {
        if (socket == null) {
            JOptionPane.showMessageDialog(this, "You not already connect with server", "Error", JOptionPane.ERROR_MESSAGE);
            btnConnect.setText("Connect");
            btnConnect.setEnabled(true);
            return false;
        }
        File f = new File(txtFilePath.getText().trim());
        if (!f.isFile()) {
            return false;
        }
        return true;
    }
    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        if (!valid()) {
            return;
        }
        File f = new File(txtFilePath.getText().trim());
        FileInfor fileInfor = new FileInfor(f);
        try {
            dos.writeBytes("&FSendFile"); // send Status to send file
            dos.write(13);
            dos.flush();
            oos = new ObjectOutputStream(socket.getOutputStream());
            oos.writeObject(fileInfor); // send File
            oos.write(13);
            oos.flush();
           
        } catch (IOException ex) {
        }

    }//GEN-LAST:event_btnSendActionPerformed

    private void tbnChooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnChooseFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(this);
        if (fileChooser.getSelectedFile() != null) {
            txtFilePath.setText(fileChooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_tbnChooseFileActionPerformed

