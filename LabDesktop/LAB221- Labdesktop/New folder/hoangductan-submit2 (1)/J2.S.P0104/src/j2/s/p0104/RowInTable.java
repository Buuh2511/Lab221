    public void dataTable() throws Exception {
        Object[] titler = {"StockId", "StockName", "Address", "DateAvailable", "Note"};
        Object[][] data = {
                {"1", "Stock one", "Ho Chi Minh", "2019-01-26", ""},
                {"2", "Stock two", "Dong Thap", "2019-11-23", ""},
                {"3", "Stock three", "Long An", "2019-12-24", ""},
                {"4", "Stock four", "Binh Dinh", "2019-01-01", ""},
                {"5", "Stock five", "Da Nang", "2019-02-01", ""}
            
        };
        tableRows = data.length;
        df.setDataVector(data, titler);
        jTable1.setModel(df);
    }
    public void moveRow(int index ){
        int[] rows = jTable1.getSelectedRows();
        int des = rows[0] + index;
        if(des < 0 || des >= tableRows){
            return;
        }
        jTable1.setRowSelectionInterval(rows[0] + index, rows[0] + index);
       
    }

