    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String code = txtAddCode.getText();
        String name = txtAddName.getText();

        if (validCourse() == false) {
            JOptionPane.showMessageDialog(this, "Invaid. Try again!!!");
            return;
        }
        int credit = Integer.parseInt(txtCredit.getText());
        Course c = new Course(code, name, credit);
        if (addNew) {
            model.getData().add(c);
        }
        try {
            PrintWriter pw = new PrintWriter(filename);
            for (int i = 0; i < this.model.getRowCount(); i++) {
                Course co = this.model.getData().elementAt(i);
                String scode = co.getCode();
                String sname = co.getName();
                int scredit = co.getCredit();

                if (!scode.isEmpty() && !sname.isEmpty()) {
                    String thisData = String.format("%s,%s,%s", scode, sname, scredit);
                    pw.println(thisData);

                }
            }
            JOptionPane.showMessageDialog(this, "add success");
            pw.close();
            changed = false;
        } catch (FileNotFoundException e) {
            e.printStackTrace();

        }
    }//GEN-LAST:event_btnAddActionPerformed

