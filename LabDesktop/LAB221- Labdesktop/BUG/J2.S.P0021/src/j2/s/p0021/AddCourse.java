    public boolean validCourse() {
        String code = txtAddCode.getText();
        String name = txtAddName.getText();
        String creditStr = txtCredit.getText();
        if (code.isEmpty() || name.isEmpty() || creditStr.isEmpty()) {
            return false;
        }
        if (!creditStr.matches("\\d+")) {
            return false;
        }
        int credit = Integer.parseInt(txtCredit.getText());
        if (credit < 0 || credit > 33) {
            return false;
        }
        return true;
    }

    public void loadData() {
        try {
            File f = new File(filename);
            if (!f.exists()) {
                return;
            }

            String detial = null;
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);

            while ((detial = br.readLine()) != null) {
                StringTokenizer line = new StringTokenizer(detial, ",");
                model.getData().add(new Course(line.nextToken(), line.nextToken(), Integer.parseInt(line.nextToken())));
            }
            br.close();
            fr.close();
        } catch (IOException e) {
                 JOptionPane.showMessageDialog(this, "The file " + filename + " not found");
        }
    }

