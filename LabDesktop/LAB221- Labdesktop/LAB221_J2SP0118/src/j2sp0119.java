    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            Connection con = null;
            PreparedStatement stm = null;
            int check = 1;
            try {
                con = MyConnection.makeConnection();
                if (con != null) {
                    for (int i = 0; i < tbStock.getRowCount(); i++) {
                        String sql = "Insert into Stocks (StockID, StockName, Address, DateValiable, Note) values(?, ?, ?, ?, ?)";
                        stm = con.prepareStatement(sql);
                        stm.setInt(1, Integer.parseInt(tblModel.getValueAt(i, 0).toString()));
                        
                        stm.setString(2, tblModel.getValueAt(i, 1).toString());
                      
                        stm.setString(3, tblModel.getValueAt(i, 2).toString());
                        
                        stm.setString(4, tblModel.getValueAt(i, 3).toString());
                       
                        if (tblModel.getValueAt(i, 4) != null) {
                            stm.setString(5, tblModel.getValueAt(i, 4).toString());
                            
                        } else {
                            stm.setNull(5, Types.NULL);
                            
                        }

                        int row = stm.executeUpdate();
                        if ((row > 0)) {
                            check++;
                        }
                    }
                }
                JOptionPane.showMessageDialog(null, "Insert Successful");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Insert failed start at row " + check);
                tbStock.setRowSelectionInterval(check, check);
                System.out.println(e.getMessage() + "1 2 3 ");
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            } finally {
                if (stm != null) {
                    stm.close();
                }
                if (con != null) {
                    con.close();
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "DB not found");
        }
    }//GEN-LAST:event_btnInsertActionPerformed

